using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace IzuzeciZad5
{
    class VectorIterator : IIterator
    {
        private Vector myVector;
        private int currentIndex;
        
        public VectorIterator(Vector v)
        {
            myVector = v;
        }
        public void reset()//--------------------------------------------------
        {//-------------------------------------------------------------------|
            currentIndex = 0;//-----------------------------------------------|
        }//-------------------------------------------------------------------|
        public bool existMore()//---------------------------------------------|
        {//-------------------------------------------------------------------|
            return currentIndex < myVector.getDimension();//------------------|
        }//-------------------------------------------------------------------|--------IIterator
        public void next()//--------------------------------------------------|
        {//-------------------------------------------------------------------|
            currentIndex++;//-------------------------------------------------|
        }//-------------------------------------------------------------------|
        public int current()//------------------------------------------------|
        {//-------------------------------------------------------------------|
            return myVector.getElements(currentIndex);//----------------------|
        }//-------------------------------------------------------------------|
    }
}
